import { db } from './firebase';
import { doc, setDoc, getDoc, collection, addDoc } from 'firebase/firestore';

export async function testFirebaseConnection() {
  console.log('ðŸ§ª Testing Firebase connection...');

  if (!db) {
    console.error('âŒ Database not initialized');
    return false;
  }

  try {
    // Test 1: Simple document write
    const testDoc = {
      test: true,
      timestamp: new Date().toISOString(),
      message: 'Firebase test successful'
    };

    console.log('ðŸ“ Testing document write...');
    await setDoc(doc(db, 'test', 'connection-test'), testDoc);
    console.log('âœ… Document write successful');

    // Test 2: Document read
    console.log('ðŸ“– Testing document read...');
    const docSnap = await getDoc(doc(db, 'test', 'connection-test'));
    if (docSnap.exists()) {
      console.log('âœ… Document read successful:', docSnap.data());
    } else {
      console.log('âŒ Document not found');
      return false;
    }

    // Test 3: Collection add (auto-generated ID)
    console.log('ðŸ“ Testing collection add...');
    const docRef = await addDoc(collection(db, 'test-collection'), {
      autoGenerated: true,
      timestamp: new Date().toISOString()
    });
    console.log('âœ… Collection add successful, ID:', docRef.id);

    console.log('ðŸŽ‰ All Firebase tests passed!');
    return true;

  } catch (error) {
    console.error('âŒ Firebase test failed:', error);

    // Check if it's a permission error
    if (error instanceof Error) {
      if (error.message.includes('permission-denied')) {
        console.error('ðŸš« Permission denied - check Firestore security rules');
        console.error('ðŸ’¡ You may need to update security rules to allow read/write');
      }
      if (error.message.includes('network')) {
        console.error('ðŸŒ Network error - check internet connection');
      }
    }

    return false;
  }
}

